package clp;

/**
 * The enum {@link OptionOrder} defines the order in which
 * options are displayed.
 * 
 * @author Ben Cisneros
 * @version 08/09/2018
 * @since 1.2
 */
public enum OptionOrder {
    
    /**
     * Display options in alphabetical order.
     */
    LEXICOGRAPHIC,
    
    /**
     * Display options in any order.
     */
    NONE
}
